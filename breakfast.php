						<h2>Directions</h2>
						<ol>
							<li>Crumble sausage into a medium skillet. Cook over medium heat until evenly brown; drain.</li>
							<li>In a medium bowl, mix together mustard powder, salt, eggs and milk. Add the sausage, 
							bread cubes, and cheese, and stir to coat evenly. Pour into a greased 9x13 inch baking dish. 
							Cover, and chill in the refrigerator for 8 hours, or overnight.</li>
							<li>Preheat oven to 350 degrees F</li>
							<li>Cover, and bake 45 to 60 minutes. Uncover, and reduce temperature to 325 degrees F Bake 
							for an additional 30 minutes, or until set.</li>
