							<li>1/2 teaspoon salt</li>
							<li>4 eggs, beaten</li>
							<li>2 cups milk</li>
							<li>6 slices white bread, toasted and cut into cubes</li>
							<li>8 ounces mild Cheddar cheese, shredded</li>
