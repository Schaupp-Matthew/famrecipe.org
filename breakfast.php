					<figcaption>Dutch Baby with Orange Sugar</figcaption>
					<content>
						<h2>Ingredients</h2>
						<h3>Orange Sugar</h3>
						<ul>
							<li>1/3 cup sugar</li>
							<li>1 orange</li>
						</ul>
						<h3>Dutch Baby Pancake</h3>
						<ul>
							<li>3 large eggs, at room temperature</li>
							<li>2/3 cup whole milk, at room temperature</li>
							<li>2/3 cup all-purpose flour</li>
							<li>1/4 teaspoon pure vanilla extract</li>
							<li>1/8 teaspoon cinnamon</li>
							<li>1/8 teaspoon ground nutmeg</li>
							<li>1/8 teaspoon salt</li>
							<li>6 tablespoons unsalted butter, cut into pieces </li>
						</ul>
						<h2>Directions</h2>
						<ol>
							<li>Put your iron skillet on the middle rack of your oven and preheat to 450 (F).</li>
							<li>As the oven is preheating, use a cheese grater to remove all the zest from your orange. 
							Combine the orange zest with 1/3 cup sugar, gently mixing the two together with a spoon. 
							The zest and sugar should begin to form small clump together.</li>
							<li> beat 3 eggs at high speed until pale and frothy. Then beat in 2/3 cup milk, 2/3 cup flour, 
							1/4 teaspoon vanilla, 1/8 teaspoon cinnamon, 1/8 teaspoon nutmeg, and 1/8 teaspoon salt and 
							continue to beat until smooth (about 1 minute more). The batter should be thin.</li>
							<li>Take your preheated skillet out of the oven and add 6 tablespoons butter, swirling 
							the pan to melt the butter and coat the pan. Once the bottom of the pan is covered in 
							butter, pour the Dutch Baby pancake batter into it and immediately return the skillet to the oven.</li>
							<li>Bake until puffed and golden-brown, for 18-25 minutes.</li>
							<li>Once the Dutch Baby is done baking in the oven, remove from heat and immediately sprinkle the 
							orange-sugar on top. The heat from the skillet/pancake will cause the sugar to melt. Serve immediately.</li>
						</ol>
