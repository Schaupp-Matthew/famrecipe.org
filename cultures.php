					<content>
						<h2>Spetzele</h2>
						<h3>Ingredients</h3>
						<ul>
							<li>2 cups all-purpose flour</li>
							<li>4 eggs, lightly beaten</li>
							<li>1/3 cup 2% milk</li>
							<li>8 cups water</li>
							<li>1 tablespoon butter</li>
							<li>Salt</li>
						</ul>
						<h3>Directions</h3>
						<ol>
							<li>In a large bowl, stir the flour, eggs, and milk until smooth (dough will be sticky). In a large 
							saucepan over high heat, bring water to a boil. Pour dough into a colander or spaetzle maker coated 
							with cooking spray; place over boiling water.</li>
							<li>With a wooden spoon, press dough until small pieces drop into boiling water. Cook for 2 minutes 
							or until dumplings are tender and float. Remove with a slotted spoon</li>
							<li>Toss with butter and season with salt to taste.</li>
						</ol>
						<h2>Red Cabbage</h2>
						<h3>Ingredients</h3>
						<ul>
							<li>2 tablespoons butter</li>
							<li>5 cups shredded red cabbage</li>
							<li>1 cup diced apples</li>
							<li>1/3 cup apple cider vinegar</li>
							<li>3 tablespoons water</li>
							<li>1/4 cup white sugar</li>
							<li>2 teaspoons salt</li>
							<li>1/4 teaspoon black pepper</li>
							<li>1/4 teaspoon ground cloves</li>
						</ul>
						<h3>Directions</h3>
						<ol>
							<li>Place butter, cabbage, apples, and sugar into a large pot. Pour in the vinegar and water, 
							and season with salt, pepper, and clove.</li>
							<li>Bring to a boil over medium-high heat, then reduce heat to low, cover, and simmer until 
							the cabbage is tender, 1 1/2 to 2 hours.</li>
						</ol>
						<h2>Rouladen</h2>
						<h3>Ingredients</h3>
						<ul>
							<li>8 slices beef top round steak (1/4 inch thick and 4 ounces each)</li>
							<li>Salt and pepper to taste</li>
							<li>8 bacon strips</li>
							<li>1 large onion, diced</li>
							<li>3 tablespoons canola oil</li>
							<li>3 cups beef broth</li>
							<li>1/3 cup all-purpose flour</li>
							<li>1/2 cup water</li>
						</ul>
						<h3>Directions</h3>
						<ol>
							<li>Sprinkle each steak with salt and pepper. Place 1 bacon strip and diced onion on each slice; 
							roll up; secure with toothpicks. </li>
							<li>In a large skillet, brown beef in oil until no longer pink; drain. Add broth; bring to a boil. 
							Reduce heat; cover and simmer for 1-1/2 hours or until meat is tender. </li>
							<li>Remove meat and keep warm. Combine flour and water until smooth; gradually stir into broth. 
							Bring to a boil, stirring constantly until thickened and bubbly. Remove toothpicks. Return beef 
							to gravy; heat through.</li>
						</ol>
