					<content>
						<h2>Ingredients</h2>
						<h3>For the shrimp marinade:</h3>
						<ul>
							<li>1/2 teaspoon kosher salt</li>
							<li>1/4 teaspoon ground black pepper</li>
							<li>1/8 teaspoon cayenne pepper</li>
							<li>2 tablespoons freshly squeezed lemon juice (from about 1 lemon)</li>
							<li>1-2 pounds large or extra-large shrimp, peeled and deveined</li>
						</ul>
						<h3>For the sauce:</h3>
						<ul>
							<li>1 tablespoon canola oil</li>
							<li>1 medium red or yellow onion, finely chopped</li>
							<li>1 teaspoon ground pepper</li>
							<li>1 tablespoon minced fresh ginger, from about a 1-inch peeled piece of ginger</li>
							<li>1 teaspoon kosher salt</li>
							<li>2 garlic cloves, peeled and finely minced</li>
							<li>2 teaspoons ground coriander</li>
							<li>1/2 teaspoon turmeric</li>
							<li>1 teaspoon curry powder</li>
							<li>1 (14.5-ounce) can diced tomatoes, undrained</li>
							<li>1 can coconut milk, light or regular</li>
							<li>1 cup chopped fresh cilantro</li>
						</ul>
						<h2>Directions</h2>
						<ol>
							<li>For the simple shrimp marinade, toss the shrimp with the salt, pepper, cayenne and lemon juice 
							in a resealable large bag or in a bowl. Refrigerate for 15-20 minutes.</li>
							<li>Meanwhile, in a medium-sized pot, heat the canola oil over medium heat until rippling and hot. 
							Add the onion and cook for 2-3 minutes. Stir in the pepper, ginger, salt, garlic, coriander, turmeric 
							and curry powder. Continue cooking over medium heat, stirring often, until the mixture is very fragrant 
							and the onion is soft and translucent, about 5-7 minutes.</li>
							<li>Add the undrained tomatoes to the pot and cook, stirring and scraping up the browned bits from the 
							bottom, for about 1 minute. Increase the heat to medium-high and simmer for 5-10 minutes, stirring often. </li>
							<li> Add the coconut milk, stirring to combine. Bring the mixture to a boil and add the shrimp and any 
							accumulated juices from the marinade. Bring the mixture to a simmer and cook until the shrimp are curled, 
							lightly pink and cooked through, about 2-4 minutes.</li>
							<li>Stir in the cilantro.  Pairs well with coconut rice.</li>
						</ol>
					</content>
