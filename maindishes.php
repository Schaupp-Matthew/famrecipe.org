						<h2>Ingredients</h2>
						<h3>For the chicken:</h3>
						<ul>
							<li>1.5 lbs boneless, skinless chicken breast</li>
							<li>2 cups buttermilk</li>
							<li>3 Tbls Honey</li>
							<li>1.5 Tbls of your favorite blackening/jerk seasoning</li>
						</ul>
						<h3>For the waffles:</h3>
						<ul>
							<li>2 cups all-purpose flour</li>
							<li>1/2 tsp salt</li>
							<li>2 Tbls sugar</li>
							<li>2 tsp blackening/jerk seasoning</li>
							<li>1 tsp cinnamon</li>
							<li>1 1/2 tsp baking soda</li>
							<li>2 eggs</li>
							<li>1 1/2 c buttermilk</li>
							<li>4 Tbls butter, melted and cooled</li>
							<li>3 Tbls real maple syrup (or 1 tsp maple extract)</li>
							<li>1/2 tsp vanilla extract</li>
						</ul>
						<h3>For Frying:</h3>
						<ul>
							<li>1/3 cup chopped pecans</li>
							<li>1 heaping cup all-purpose flour</li>
							<li>1 Tbls blackening/jerk seasoning</li>
							<li>1 Tbls white pepper</li>
							<li>1 Tbls salt</li>
							<li>Vegetable oil</li>
						</ul>
						<h2>Directions</h2>
						<p>Reminder - it does not matter if you fry the chicken or make the waffles first.
						<ol>
							<li>In between two pieces of seran wrap, pound out the chicken to about 1/2" - 3/4" thick. 
							You can skip this step if you are lucky enough to find your chicken pre-thinned when you buy it.</li>
							<li>In a gallon-sized plastic bag, combine the buttermilk, honey, and seasoning. Zip the top 
							and shake around to combine. Unzip, and add the chicken. Seal the bag and turn to coat the chicken. 
							Place in the fridge for at least 3 hours (up to 1 day).</li>
						</ol>
						<h3>For the Waffles:</h3>
						<ol>
							<li>Heat up your waffle iron</li>
							<li>In a large bowl, combine the dry ingredients.</li>
							<li>In a medium bowl, whisk together the wet ingredients. Pour into the dry ingredients and stir just to combine.</li>
							<li>Brush the waffle iron lightly with oil or butter.</li>
							<li>Pour the batter into the iron. Close and cook until done.</li>
							<li>If making these first, slide into a warmed oven (about 200-225 degrees).</li>
						</ol>
						<h3>For the Chicken:</h3>
						<ol>
							<li>In a food processor, pulse the pecans until they are very fine (pecan flour). Set aside.</li>
							<li>Place a large skillet over medium-high heat and add enough oil to fill about 1/2" in the pan</li>
							<li>whisk the pecan flour, all-purpose flour, seasoning, pepper, and salt together.</li>
							<li>dredge the chicken, one-piece at time, and place in the oil. Cook about 4 minutes per side until brown and crispy.</li>
							<li>Repeat with remaining chicken.</li>
							<li>Keep warm in the oven.</li>
						</ol>
						<p>To serve, Top the waffles with butter and syrup, and spicy brown mustard.</p>
