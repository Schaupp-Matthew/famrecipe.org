					<content>
						<h3>Ingredients</h3>
						<ul>
							<li>2 large Granny Smith apples, peeled and cored</li>
							<li>2 (10 ounce) cans refrigerated crescent roll dough</li>
							<li>1 cup butter</li>
							<li>1/2 cups white sugar</li>
							<li>1 teaspoon ground cinnamon</li>
							<li>1 (12 fluid ounce) can or bottle Ginger ale</li>
							<li>1/4 cup brown sugar</li>
						</ul>
						<h3>Directions</h3>
						<ol>
							<li>Preheat the oven to 350 degrees F. Grease a 9x13 inch baking dish.</li>
							<li>Cut each apple into 8 wedges and set aside. Separate the crescent roll dough into triangles. 
							Roll each apple wedge in crescent roll dough starting at the smallest end. Pinch to seal and place in the baking dish.</li>
							<li>Melt butter in a small saucepan and stir in the sugar and cinnamon. Pour over the apple dumplings. Pour ginger ale over the dumplings.</li>
							<li>Bake for 35 to 45 minutes in the preheated oven, or until golden brown.</li>
						</ol>
