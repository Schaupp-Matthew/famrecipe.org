					<figcaption><h2>Caramelized Butternut Squash</h2></figcaption>
					<content>
						<h3>Ingredients</h3>
						<ul>
							<li>2 medium butternut squash (4 to 5 pounds total)</li>
							<li>6-8tablespoons  unsalted butter, melted and cooled</li>
							<li>1/4 cup  light brown sugar, packed</li>
							<li>1 1/2 teaspoons  kosher salt</li>
							<li>1/2-1 teaspoon  fresh ground black pepper</li>
						</ul>
						<h3>Directions</h3>
						<ol>
							<li>Preheat the oven to 400 degrees F.</li>
							<li>Cut off the ends of each butternut squash and discard.</li>
							<li>Peel the squash and cut in half lengthwise.</li>
							<li>Using a spoon, remove the seeds.</li>
							<li>Cut the squash into 1 1/4 inch to 1 1/2 inch cubes (large and uniform is best), and place them on a baking sheet.</li>
							<li>Add the melted butter, brown sugar, salt and pepper.</li>
							<li>With clean hands, toss all of the ingredients together and spread out in a single layer on the baking sheet.</li>
							<li>Roast for 45 minutes to 55 minutes, until the squash is tender and the glaze begins to caramelize.</li>
							<li>Turn the squash while roasting a few times with a spatula to be sure it browns evenly.</li>
						</ol>
					</content>
