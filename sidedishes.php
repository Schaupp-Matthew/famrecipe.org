					<figcaption>Coconut Rice</figcaption>
					<content>
						<h2>Ingredients</h2>
						<ul>
							<li>1 14 oz can of coconut milk (about 2 cups)</li>
							<li>1/2 cups water</li>
							<li>1 teaspoon salt</li>
							<li>3/4 teaspoon sugar</li>
							<li>1 cup long grain white rice</li>
							<li>3 tablespoons chopped fresh cilantro</li>
						</ul>
						<h2>Directions</h2>
						<ol>
							<li>In a saucepan, combine the coconut milk, water, salt, and sugar. Bring to a boil.</li>
							<li>Add the rice, stir well, and reduce the heat to medium-low. Cover and simmer undisturbed until 
							the liquid is absorbed and the rice is fluffy, about 20 to 24 minutes. Remove from the heat and let 
							sit without stirring for 10 minutes.</li>
							<li> Fluff with a fork and add cilantro.</li>
						</ol>
					</content>
