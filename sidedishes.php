					<img src="imagefiles/mac.jpg" class="large" />
					<figcaption>Macaroni and Cheese</figcaption>
					<content>
						<h2>Ingredients</h2>
						<ul>
							<li>8 oz. macaroni</li>
							<li>3 tablespoons butter</li>
							<li>1/4 cup all-purpose flour</li>
							<li>1/2 teaspoon salt</li>
							<li>1/2 teaspoon dry mustard</li>
							<li>1/4 teaspoon black pepper</li>
							<li>1/8 teaspoon smoked paprika plus more for sprinkling on top</li>
							<li>2 cups 2% milk</li>
							<li>1/2 cup sour cream</li>
							<li>3 cups grated sharp cheddar cheese</li>
							<li>1 cup grated pepper jack cheese</li>
						</ul>
						<h2>Directions</h2>
						<ol>
							<li>Boil the pasta until al dente, according to package directions. Drain and rinse with cool water 
							to stop the cooking, and return to the pan you boiled it in. Set aside. (Mix in a little butter to 
							keep the noodles from sticking if it's going to be awhile before you make the sauce)</li>
							<li>Preheat the oven to 375 degrees</li>
							<li>In a medium saucepan over medium heat, add the butter. When the butter has melted, add the flour, 
							salt, dry mustard, pepper, and 1/8 teaspoon smoked paprika. With a wooden spoon or whisk, stir constantly 
							for three minutes.</li>
							<li>Add the milk in a thin stream, stirring constantly with a whisk. Continue to stir constantly until the 
							sauce thickens, about 10-12 minutes.</li>
							<li>Remove from heat. Add sour cream until combine.</li>
							<li>Add 1 cup of cheddar cheese and all of the pepper jack cheese, stirring until melted. Pour the cheese 
							sauce over the pasta and toss gently until all of the pasta is coated.</li>
							<li>Add half the pasta to a two-quart casserole dish .  Sprinkle on 1 cup of cheddar cheese. Add the rest 
							of the pasta. Sprinkle on the rest of the cheese, then dust lightly with the smoked paprika.</li>
							<li>Bake for about 25-30 minutes.</li>
						</ol>
					</content>
